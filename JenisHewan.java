package inheritance;

public class JenisHewan {
	    String warnakulit;
	    int jumlahkaki;
	   
	    void warnakulit (String warna) {
	        warnakulit=warna;
	        System.out.println ("Warna Kulitnya : " + warnakulit);
	    }
	   
	    void jumlahkaki (int jumlah)
	    {
	        jumlahkaki=jumlah;
	        System.out.println ("Jumlah Kakinya : " + jumlahkaki);
	    }
}
